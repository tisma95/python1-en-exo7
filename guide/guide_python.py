def sum_product(a,b):
    """ Computes the sum and product of two numbers. """
    s = a + b
    p = a * b
    return s, p

# Call of the function
mysum, myprod = sum_product(6,7)  # Results
print("The sum is:",mysum)        # Display
print("The product is:",myprod)   # Display